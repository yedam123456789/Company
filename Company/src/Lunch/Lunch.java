package Lunch;

public class Lunch {

	private String fruitName;
	private String fruitPrice;
	private int lunchBoxCount;
	
	
	public String getFruitName() {
		return fruitName;
	}
	public void setFruitName(String fruitName) {
		this.fruitName = fruitName;
	}
	public String getFruitPrice() {
		return fruitPrice;
	}
	public void setFruitPrice(String fruitPrice) {
		this.fruitPrice = fruitPrice;
	}
	public int getLunchBoxCount() {
		return lunchBoxCount;
	}
	public void setLunchBoxCount(int lunchBoxCount) {
		this.lunchBoxCount = lunchBoxCount;
	}
	
	
	
}
